# Hexadecimal
# 16 instead of 10 digits
print(hex(12))

# Binary Number
print(bin(128))

# Power
print(2 ** 4)
print(pow(2, 4))
# 3 arguments: third arg = mod (%)
print(pow(2, 4, 3))

# Absolute Value
print(abs(-5))

# round (to a given decimal point)
print(round(3.9))  # default = 0
print(round(3.141592, 2))
